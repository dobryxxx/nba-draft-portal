export function normalizeProspectName(name) {
  return String(name || '')
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .toLowerCase()
    .replace(/jr\.?$/, '')
    .replace(/[^a-z0-9]/g, '');
}

export function getExternalStatsForProspect(prospect) {
  const key = normalizeProspectName(prospect?.name);
  return externalProspectStats.find(item => item.key === key) || null;
}

export function getMergedProspectStats(prospect, { fillOnly = true } = {}) {
  const external = getExternalStatsForProspect(prospect);
  if (!external) return prospect?.stats || {};
  if (!fillOnly) return { ...(prospect?.stats || {}), ...external.stats };
  const merged = { ...(prospect?.stats || {}) };
  Object.entries(external.stats || {}).forEach(([key, value]) => {
    if ((merged[key] === null || merged[key] === undefined || merged[key] === '') && value !== null && value !== undefined) {
      merged[key] = value;
    }
  });
  return merged;
}

export function getProspectStatConflicts(prospect, tolerance = 0.6) {
  const external = getExternalStatsForProspect(prospect);
  if (!external) return [];
  const current = prospect?.stats || {};
  return Object.entries(external.stats || {}).flatMap(([key, externalValue]) => {
    const currentValue = current[key];
    if (typeof currentValue !== 'number' || typeof externalValue !== 'number') return [];
    return Math.abs(currentValue - externalValue) > tolerance ? [{ key, current: currentValue, external: externalValue }] : [];
  });
}
